# Integers

print(2+3)
print(3-2)
print(2*3)
print(3/2)

print(2+3*4)
print((2+3)*4)

# Exponents

print(3**2)
print(3**3)
print(10**6)

# Floats

print(.1+.1)
print(.2+.2)
print(2*.1)
print(2*.2)

# When you divide or mix integers w/ float, you will always get a float

print(4/2)
print(1+2.0)
print(2.0*3)
print(3.0**2)

# When writing long numbers, can use underscores to group digits, but Python prints & stores w/o them

universe_age = 14_000_000_000
print(universe_age)

# Multiple Assignment

x, y, z = 1, 2, 3

print(x+y-z)

